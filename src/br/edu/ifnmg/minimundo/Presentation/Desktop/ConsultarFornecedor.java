    private void preencherTabela(List<Fornecedor> lista) {
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("ID");
        modelo.addColumn("Razão Social");
        modelo.addColumn("CNPJ");
        modelo.addColumn("Email");
        modelo.addColumn("Estado");
        modelo.addColumn("Cidade");
        modelo.addColumn("Cep");
        modelo.addColumn("Bairro");
        modelo.addColumn("Rua");
        modelo.addColumn("Numero");
        modelo.addColumn("Complemento");
        
        for(Fornecedor a : lista){
            Vector linha = new Vector();
            linha.add(a.getId());
            linha.add(a.getRs());
            linha.add(a.getcnpj());
            linha.add(a.getEmail());
            linha.add(a.getEstado());
            linha.add(a.getCidade());
            linha.add(a.getCep());
            linha.add(a.getBairro());
            linha.add(a.getRua());
            linha.add(a.getNumero());
            linha.add(a.getComplemento());
            
            modelo.addRow(linha);
        }
       
        tabResultado.setModel(modelo);
    }
